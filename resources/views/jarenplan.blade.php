                        verplichtingen van het VN-verdrag handicap. BOZ wil de komende jaren het gesprek
                        aangaan en adviseren en werken aan spraakmakende producties, om zaken
                        inzichtelijk te krijgen en daardoor bewustwording te creëren bij de
                        bevolking.</p>
