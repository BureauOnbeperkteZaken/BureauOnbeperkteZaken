                    <p><span
                        ><strong>Meedenken over wetten en beleid</strong></span>
                    </p>
                    <p>&nbsp;</p>
                    <p>Het is belangrijk dat mensen met een beperking meer en eerder meedenken over
                        wetten en beleid. Zo moeten zij bijvoorbeeld meepraten over onderwijs, werk,
                        wonen of zorg. Alleen mensen met een beperking zelf kunnen namelijk vertellen
                        wat zij meemaken in hun leven en wat zij nodig hebben. Marjolein Swaanenburg
                        –van Roosmalen (Collegelid): “Het actief betrekken van ervaringsdeskundigen is
                        niet vrijblijvend, maar een verdragsverplichting. Participatie draagt niet
                        alleen bij aan de kwaliteit en effectiviteit van beleid en wetgeving, maar zorgt
                        ook voor een toegankelijke samenleving waarin iedereen écht kan meedoen!”</p>
                    <p>&nbsp;</p>
                    <p><span
                        ><strong>Niet of veel te laat betrokken</strong></span>
                    </p>
                    <p>&nbsp;</p>
                    <p>Ondanks het feit dat de regering en gemeenten verplicht zijn om mensen met een
                        beperking te betrekken, gebeurt dit vaak nog steeds niet of pas veel te laat.
                        Pauline Gransier (ervaringsdeskundige): “Het klinkt allemaal zo voor de hand
                        liggend. Waarom zou je als je beleid maakt voor mensen met een beperking hen er
                        niet bij betrekken? Een Afrikaans gezegde luidt: ‘Wat je niet met ons doet, doe
                        je tegen ons.’ Zo is het precies, en toch worden we veel te weinig gevraagd.
                        Daarnaast worden we zelden bij het begin van het traject erbij betrokken.
                        Vrijwel altijd mogen we helemaal aan het einde nog even ons zegje doen.”</p>
                    <p>&nbsp;</p>
                    <p><span><strong>Uitdagingen BOZ</strong></span></p>
                    <p>&nbsp;</p>
                    <p>Bureau Onbeperkte Zaken, wilt breed kijken. Om dit allemaal te verwezenlijken
                        moet je het totale plaatje kunnen zien. De juiste context anders blijven we maar
                        dromen over inclusie en participatie die nauwelijks van de grond komt. We moeten
                        kijken naar alle partijen die betrokken zijn en kunnen worden, om te komen tot
                        een echte inclusieve maatschappij. Wij willen de vertaalslag maken door middel
                        van onze creatieve projecten; film, theater, zang, kunst etc.</p>
                    <p>&nbsp;</p>
                    <p>Wij stellen de mensen met een beperking centraal, Daar omheen Het VN-verdrag,
                        vervolgens de buitenwereld waar mensen met een beperking mee te maken
                        hebben.</p>
