                    <h3><span><strong>VN-verdrag versus BOZ</strong></span>
                    </h3>
                    <p>&nbsp;</p>
                    <p>Ruim 2 miljoen mensen in ons land hebben een beperking. Zij zijn bijvoorbeeld
                        slechtziend, doof, hebben een fysieke beperking of zijn psychisch kwetsbaar.
                        Sinds 2016 geldt het&nbsp;<a
                            href="https://mensenrechten.nl/nl/vn-verdrag-handicap">VN-verdrag handicap
                            in Nederland</a>. Dit verdrag verplicht de overheid ervoor te zorgen dat
                        mensen met een beperking kunnen leven als ieder ander in ons land. In het
                        verdrag staat wat de regering hiervoor moet doen. Het College is toezichthouder
                        op de uitvoering van het verdrag en kijkt of iedereen zich aan de afspraken
                        houdt.</p>
                </div>
