                    <h3><span
                        ><strong>Wie zijn wij</strong></span>
                    </h3>
                    <p>Er zijn in de geschiedenis altijd argumenten bedacht door mensen, waarom mensen
                        met een beperking/handicap echt anders zijn, zo niet minder waren. Het zijn
                        sociale bedenksels. Ze vertrekken bij standpunten tussen normaal en niet
                        normaal, ze zien die terug in het lichaam en vervolgens verklaren ze het vanuit
                        het lichaam. Maar het zijn projecties.</p>
                    <p>&nbsp;</p>
                    <p>Wij werken volgens het VN-verdrag. Het doel van dit Verdrag is het volledige
                        genot door alle personen met een beperking/handicap van alle mensenrechten en
                        fundamentele vrijheden op voet van gelijkheid te bevorderen, beschermen en
                        waarborgen, en ook de eerbiediging van hun inherente waardigheid te bevorderen.
                        Personen met een handicap omvat personen met langdurige fysieke, mentale,
                        intellectuele of zintuiglijke beperkingen die hen in wisselwerking met diverse
                        drempels kunnen beletten volledig, effectief en op voet van gelijkheid met
                        anderen te participeren in de samenleving.</p>
                    <p>&nbsp;</p>
                    <p>Iedereen in Nederland moet kunnen vertrouwen op hun goede rechten en at deze
                        worden nageleefd.</p>
                    <p>&nbsp;</p>
                    <p>Bij onze werkwijze staat het VN-Verdrag centraal. Wij willen met onze creatieve
                        projecten confronteren revolutioneren.</p>
                    <p>&nbsp;</p>
                    <p>We handelen vanuit het belang van de mensen met een beperking/handicap en streven
                        naar toezicht met effect. Daarom kiezen we in ons onafhankelijkheid voor thema’s
                        en activiteiten die het meest opleveren voor mensen met een handicap en die ook
                        leven bij deze mensen.</p>
                    <p>&nbsp;</p>
                    <p>We zijn open en eerlijk over hoe we die kwaliteit beoordelen, de dilemma’s die we
                        tegenkomen en de keuzes die we dan maken. We verstaan ons vak, kennen de
                        praktijk en spelen in op ontwikkelingen in zorg en samenleving. We werken samen
                        waar dat kan, maar ons oordeel is altijd onafhankelijk. Zo maken wij onze
                        verantwoordelijkheid als Bureau Onbeperkte Zaken waar.</p>
